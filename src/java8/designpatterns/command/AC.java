package java8.designpatterns.command;

public class AC {

  public void turnOn() {
    System.out.println("Turning on AC");
  }

  public void turnOff() {
    System.out.println("Turning off AC");
  }

  public void upTemperature() {
    System.out.println("up temperature");
  }

  public void downTemperature() {
    System.out.println("down temperature");
  }

}
